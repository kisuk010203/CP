#include <bits/stdc++.h>
using namespace std;
int main() {
    ios::sync_with_stdio(false);
    cin.tie(NULL);
    int T; cin >> T;
    unordered_map<int, vector<pair<int, int>>> mp;
    mp[3] = {{1, 3}, {2, 3}}; // 9
    mp[4] = {{1, 2}, {1, 3}, {1, 4}}; // 9
    mp[5] = {{1, 3}, {2, 3}, {4, 3}, {5, 3}};
    mp[6] = {{1, 3}, {2, 3}, {4, 1}, {5, 1}, {6, 3}};
    mp[7] = {{1, 3}, {2, 3}, {4, 1}, {5, 2}, {6, 1}, {7, 1}};
    mp[8] = {{1, 3}, {2, 3}, {4, 1}, {5, 3}, {6, 1}, {7, 1}, {8, 1}};
    mp[9] = {{1, 3}, {2, 3}, {4, 1}, {5, 1}, {6, 1}, {7, 1}, {8, 3}, {9, 1}};
    mp[10] = {{1, 3}, {2, 3}, {4, 1}, {5, 1}, {6, 1}, {7, 1}, {8, 4}, {9, 8}, {10, 9}};
    mp[11] = {{1, 3}, {2, 3}, {4, 3}, {5, 4}, {6, 5}, {7, 6}, {8, 7}, {9, 8}, {10, 9}, {11, 10}};
    mp[12] = {{1, 3}, {2, 3}, {4, 1}, {5, 1}, {6, 3}, {7, 6}, {8, 7}, {9, 7}, {10, 9}, {11, 10}, {12, 11}};
    mp[13] = {{1, 3}, {2, 3}, {4, 3}, {5, 4}, {6, 5}, {7, 6}, {8, 7}, {9, 8}, {10, 9}, {11, 10}, {12, 11}, {13, 12}};
    mp[14] = {{1, 3}, {2, 3}, {4, 1}, {5, 1}, {6, 1}, {7, 4}, {8, 7}, {9, 7}, {10, 9}, {11, 10}, {12, 11}, {13, 12}, {14, 13}};
    while (T--) {
        int n; cin >> n;
        if (n == 2) {
            cout << "-1\n";
            continue;
        }
        else if (n <= 14) {
            for (auto p : mp[n]) {
                cout << p.first << " " << p.second << "\n";
            }
        } else {
            if (n % 2 == 0) {
                for (auto p : mp[14]) {
                    cout << p.first << " " << p.second << "\n";
                }
                for (int i = 15; i <= n; i++) {
                    cout << 8 << " " << i << "\n";
                }
            } else {
                for (auto p : mp[13]) {
                    cout << p.first << " " << p.second << "\n";
                }
                for (int i = 14; i <= n; i++) {
                    cout << 8 << " " << i << "\n";
                }
            }
        }
        
    }
    return 0;
}
